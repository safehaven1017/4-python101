def madlib(noun, verb, name):
    print(f"""In the middle of nowhere, a {noun} sprung from the ground.
    This {noun} was so amazing that it began to {verb} everywhere. When 
    the infamous {name} caught wind of the {noun}'s awesomeness, {name} 
    began to cry tears of joy.
    """)


noun = input("Enter a noun here: ")
verb = input("Enter a present-tense verb here: ")
name = input("Enter a name here: ")

madlib(noun, verb, name)

